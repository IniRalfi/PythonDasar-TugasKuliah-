# Meminta data dasar mahasiswa
# Susun algoritma dan buat program yang meminta data dasar mahasiswa (mis: Nama, Alamat, e_mail, dan telepon) kemudian menampilkan-nya kembali tersusun.
nama_mahasiswa = input('Masukkan nama mahasiswa : ')
